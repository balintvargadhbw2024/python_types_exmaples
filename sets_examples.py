# Example 1: Creating a set of unique integers
numbers_set = {1, 2, 3, 4, 5}
print(numbers_set)

# Example 2: Adding elements to a set
colors_set = {'red', 'green', 'blue'}
colors_set.add('yellow')
print(colors_set)

# Example 3: Set operations (intersection)
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}
intersection_set = set1.intersection(set2)
print(intersection_set)
