# Example 1: Creating a list of integers
numbers = [1, 2, 3, 4, 5]
print(numbers)

# Example 2: Adding elements to a list
fruits = ['apple', 'orange', 'banana']
fruits.append('grape')
print(fruits)

# Example 3: Accessing and modifying list elements
colors = ['red', 'green', 'blue']
print(colors[1])  # Accessing the second element
colors[1] = 'yellow'  # Modifying the second element
print(colors)
