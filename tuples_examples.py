# Example 1: Creating a tuple of mixed data types
person = ('John', 30, 'Male')
print(person)

# Example 2: Unpacking a tuple
coordinates = (3, 4)
x, y = coordinates
print(f"X: {x}, Y: {y}")

# Example 3: Concatenating tuples
tuple1 = (1, 2, 3)
tuple2 = ('a', 'b', 'c')
combined_tuple = tuple1 + tuple2
print(combined_tuple)
